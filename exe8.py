# pedindo uma medida em metros
metros = float(input('Medida: '))
# convertendo tanto para centimetros quanto para milimetros
cm = metros * 100
ml = metros * 1000
# mostrando os valores convertido
print(f'Sua Medida: {metros}')
print(f'Convertida em centimetros: {cm}')
print(f'Convertido em milimetro: {ml}')
