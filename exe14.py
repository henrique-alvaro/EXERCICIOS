# pedindo a tempertura em grau celcius
celsius = float(input('Temperatura em Celsius: '))
# convertendo temperatura em celcius para fahrenheit
fah = (celsius * 1.8) + 32
# mostrando temperatura de celcius convertido para fahrenheit
print(f'Temperatura em Celsius: {celsius}, convetido para fahrenheit fica: {fah:.2fk}')
